// operadores de comparação (comparison) (retorna boolean)

let one = 1
let two = 2

console.log(one == two) //prints false
console.log(one != two) //prints true
console.log(one === two) //prints false
console.log(one === 1) //prints true
console.log(one !== two) //prints true
console.log(one > two) //prints false
console.log(one < two) //prints true
console.log(one >= two) //prints false
console.log(one <= two) //prints true

// == igual
// != diferente
// === estritamente igual
// !== estritamente diferente
// > maior
// >= maior igual
// < menor
// <= menor igual